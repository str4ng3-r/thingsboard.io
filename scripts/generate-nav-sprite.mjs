import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { basename, dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

// Builds the navigation icon sprite + its manifest from
// src/assets/images/landings/nav/*.svg:
//
//   public/nav-sprite.svg                              — <symbol> per icon
//   src/assets/images/landings/nav-sprite.manifest.json — name → { id, viewBox }
//
// The mega-menu references icons via <use href="/nav-sprite.svg#…"> (see
// NavIcon.astro), which keeps ~100KB of icon markup out of every page's HTML
// stream. NavIcon reads the manifest, so geometry and viewBox always come
// from the same generation pass. Runs automatically before builds (see
// package.json build scripts); after editing an icon in dev, re-run:
//
//   pnpm generate:nav-sprite

// Anchor paths to the repo root (not the cwd) so the script works from any
// directory — same pattern as scripts/generate-redirects.ts.
const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const SRC_DIR = join(ROOT, 'src/assets/images/landings/nav');
const OUT_SPRITE = join(ROOT, 'public/nav-sprite.svg');
const OUT_MANIFEST = join(ROOT, 'src/assets/images/landings/nav-sprite.manifest.json');
const MARKER = 'generated by scripts/generate-nav-sprite.mjs — do not edit';

// Attributes that must not be copied from a source <svg> root onto its
// <symbol>: sizing is controlled by the referencing <svg>, and identity /
// namespace attributes don't belong on symbols.
const DROP_ATTRS = new Set(['width', 'height', 'id', 'class', 'xmlns', 'xmlns:xlink', 'version']);

const files = readdirSync(SRC_DIR)
	.filter((f) => f.endsWith('.svg'))
	.sort();

const symbols = [];
const manifest = {};
for (const file of files) {
	const name = basename(file, '.svg');
	let raw = readFileSync(join(SRC_DIR, file), 'utf8').replace(/<\?xml[^>]*\?>\s*/, '');

	const match = raw.match(/<svg([^>]*)>([\s\S]*)<\/svg>\s*$/);
	if (!match) throw new Error(`${file}: could not parse <svg> root`);
	const [, rootAttrs, inner] = match;

	// Prefix internal ids per icon so defs (gradients, clip paths) from
	// different icons can't collide inside the combined sprite.
	const prefixed = inner
		.replace(/\bid="([^"]+)"/g, `id="${name}--$1"`)
		.replace(/url\(#([^)]+)\)/g, `url(#${name}--$1)`)
		.replace(/\b(xlink:href|href)="#([^"]+)"/g, `$1="#${name}--$2"`);

	// Carry presentation attributes (fill="none", stroke rules, viewBox…)
	// onto the <symbol>; they inherit into its content when instantiated.
	const kept = [];
	let viewBox = '';
	for (const attrMatch of rootAttrs.matchAll(/([a-zA-Z_:][-a-zA-Z0-9_:.]*)="([^"]*)"/g)) {
		const [, attrName, value] = attrMatch;
		if (DROP_ATTRS.has(attrName)) continue;
		if (attrName === 'viewBox') viewBox = value;
		kept.push(`${attrName}="${value}"`);
	}
	if (!viewBox) {
		throw new Error(
			`${file}: missing viewBox — the sprite needs it for scaling. Note: root ` +
				`attributes must use double quotes; single-quoted attributes are not parsed.`
		);
	}

	const id = `nav-${name}`;
	symbols.push(`<symbol id="${id}" ${kept.join(' ')}>${prefixed.trim()}</symbol>`);
	manifest[name] = { id, viewBox };
}

const sprite = `<!-- ${MARKER} -->\n<svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink">${symbols.join('\n')}</svg>\n`;
writeFileSync(OUT_SPRITE, sprite);
writeFileSync(
	OUT_MANIFEST,
	`${JSON.stringify({ '//': MARKER, icons: manifest }, null, '\t')}\n`
);
console.log(
	`✓ nav-sprite.svg (${(sprite.length / 1024).toFixed(0)} KB) + manifest — ${symbols.length} icons`
);
